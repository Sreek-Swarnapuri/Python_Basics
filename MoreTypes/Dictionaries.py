"""
## Dictionaries

Dictionaries are data structures used to map arbitrary keys to values.
Lists can be thought of as dictionaries with **integer** keys within a certain range.
Dictionaries can indexed in the same way as lists, using **square brackers** containing keys.
"""

ages = {"Dave":24,"Mary":42,"Kevin":22}
print(ages)
print(ages["Dave"])
print(ages["Mary"])

# Output:
# {'Dave': 24, 'Mary': 42, 'Kevin': 22}
# 24
# 42
